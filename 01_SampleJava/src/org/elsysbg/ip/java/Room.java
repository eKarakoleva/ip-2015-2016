package org.elsysbg.ip.java;

public class Room {
	
	// define fields
	private int width;
	private int height;

	// getters and setters:
	public int getWidth() {
		return width;
	}
	public void setWidth(int width) {
		this.width = width;
	}
	public int getHeight() {
		return height;
	}
	public void setHeight(int height) {
		this.height = height;
	}
}
